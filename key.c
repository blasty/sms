#include <GLUT/glut.h>

void key_pressed( unsigned char key, int x, int y ) {
	switch(key) {
		case 'z': //b button
		break;

		case 'x': //a button
		break;

		case '/': //select button
		break;

		case 13: //start button
		break;

		case 'q': //activate debugger
		break;
	}
}

void key_released( unsigned char key, int x, int y ) {
	switch(key) {
		case 'z': //b button
		break;

		case 'x': //a button
		break;

		case '/': //select button
		break;

		case 13: //start button
		break;
	}
}

void key_special_pressed( int key, int x, int y ) {
	switch(key) {
		case GLUT_KEY_UP:
		break;

		case GLUT_KEY_DOWN:
		break;

		case GLUT_KEY_LEFT:
		break;

		case GLUT_KEY_RIGHT:
		break;
	}
}

void key_special_released( int key, int x, int y ) {
	switch(key) {
		case GLUT_KEY_UP:
		break;

		case GLUT_KEY_DOWN:
		break;

		case GLUT_KEY_LEFT:
		break;

		case GLUT_KEY_RIGHT:
		break;
	}
}

